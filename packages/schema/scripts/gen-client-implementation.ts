import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'node:url';
import { clientRegistry } from '../src/registry';

import '../src/methods';

const generatedDir = path.join(fileURLToPath(new URL('.', import.meta.url)), '../src/generated');
if (!fs.existsSync(generatedDir)) {
  fs.mkdirSync(generatedDir, { recursive: true });
}

type MethodMeta = ReturnType<typeof clientRegistry.getAll>[number]['meta'];

function sortStrings(values: string[]) {
  return [...values].sort((left, right) => left.localeCompare(right));
}

function buildFlatAliasDocs(meta: MethodMeta): string {
  const aliases = sortStrings((meta.allAliases ?? []).filter((alias) => !alias.includes('.')));
  return aliases.length > 0 ? `   * @aliases ${aliases.join(', ')}\n` : '';
}

function buildFlatAliasProperties(methods: ReturnType<typeof clientRegistry.getAll>): string {
  const lines: string[] = [];

  for (const def of methods) {
    const flatAliases = sortStrings((def.meta.allAliases ?? []).filter((alias) => !alias.includes('.')));

    for (const alias of flatAliases) {
      const deprecated = (def.meta.deprecatedAliases ?? []).includes(alias);
      const doc = deprecated
        ? `  /**\n   * @deprecated Use ${def.meta.functionName} instead.\n   * Deprecated alias of ${def.meta.functionName}.\n   */\n`
        : `  /**\n   * Alias of ${def.meta.functionName}.\n   */\n`;

      lines.push(`${doc}  public ${alias} = this.${def.meta.functionName};\n`);
    }
  }

  return lines.join('\n');
}

function buildNamespacedSurface(methods: ReturnType<typeof clientRegistry.getAll>): string {
  const namespaceMembers = new Map<string, Map<string, string>>();

  for (const def of methods) {
    const namespace = def.meta.namespace;
    if (!namespace) {
      continue;
    }

    const members = namespaceMembers.get(namespace) ?? new Map<string, string>();
    for (const alias of sortStrings((def.meta.allAliases ?? []).filter((entry) => entry.includes('.')))) {
      const [aliasNamespace, ...rest] = alias.split('.');
      if (aliasNamespace !== namespace) {
        continue;
      }

      members.set(rest.join('.'), def.meta.functionName);
    }

    namespaceMembers.set(namespace, members);
  }

  return sortStrings(Array.from(namespaceMembers.keys()))
    .map((namespace) => {
      const members = namespaceMembers.get(namespace)!;
      const body = sortStrings(Array.from(members.keys()))
        .map((member) => `    ${member}: this.${members.get(member)}`)
        .join(',\n');

      return `  public ${namespace} = {\n${body}\n  };\n`;
    })
    .join('\n');
}

const methods = clientRegistry.getAll();

let baseClientOutput = `/* eslint-disable */
/**
 * This file was automatically generated by packages/schema/scripts/gen-client-implementation.ts
 * Do not edit this file manually.
 */
import { implementMethod } from '../implementor';
import * as Methods from '../methods';

export abstract class BaseClient {
  protected abstract execute(method: string, args: any): Promise<any>;

  protected async pup(_func: any, args: any): Promise<any> {
    throw new Error('BaseClient.pup is not available without a runtime implementation.');
  }

`;

for (const def of methods) {
  baseClientOutput += `  /**\n   * ${def.meta.description || def.meta.functionName}\n${buildFlatAliasDocs(def.meta)}   */\n  public ${def.meta.functionName} = implementMethod(Methods.${def.meta.functionName});\n\n`;
}

baseClientOutput += `${buildFlatAliasProperties(methods)}}\n`;

const namespacedOutput = `/* eslint-disable */
/**
 * This file was automatically generated by packages/schema/scripts/gen-client-implementation.ts
 * Do not edit this file manually.
 */
import { BaseClient } from './BaseClient';

export abstract class BaseNamespacedClient extends BaseClient {
${buildNamespacedSurface(methods)}}
`;

const aliasMapOutput = `/* eslint-disable */
/**
 * This file was automatically generated by packages/schema/scripts/gen-client-implementation.ts
 * Do not edit this file manually.
 */
export const clientMethodAliasMap = ${JSON.stringify(clientRegistry.getAliasMap(), null, 2)} as const;
`;

fs.writeFileSync(path.join(generatedDir, 'BaseClient.ts'), baseClientOutput);
fs.writeFileSync(path.join(generatedDir, 'BaseNamespacedClient.ts'), namespacedOutput);
fs.writeFileSync(path.join(generatedDir, 'AliasMap.ts'), aliasMapOutput);
fs.writeFileSync(
  path.join(generatedDir, 'index.ts'),
  `/* eslint-disable */\nexport * from './AliasMap';\nexport * from './BaseClient';\nexport * from './BaseNamespacedClient';\nexport * from './types';\n`
);

console.log('Successfully generated BaseClient.ts, BaseNamespacedClient.ts, and AliasMap.ts');
